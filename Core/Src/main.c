		  //HAL_UART_Transmit(&huart1, (uint8_t*) my_message, strlen(my_message), 10);
		  //sprintf(my_message,"Duty Cycle 2 is: %ld (%ld percent).\r\n", duty_count2, duty_percent2);
		  //HAL_UART_Transmit(&huart1, (uint8_t*) my_message, strlen(my_message), 10);
		  sprintf(my_message,"\r\n");
		  HAL_UART_Transmit(&huart1, (uint8_t*) my_message, strlen(my_message), 10);
